            <script>
              $(function(){
                $('.gender_label').click(function(){
                  var val = $(this).data('value');
                  $('input[name=gender]').val(val);
                });
                $('.cancer_related_label').click(function(){
                  var val = $(this).data('value');
                  $('input[name=cancer_related]').val(val);
                  if(val==1){
                    $('textarea[name=cancer_related_txt]').addClass('required');
                  }else{
                    $('textarea[name=cancer_related_txt]').removeClass('required');
                  }

                });
                $('.language_label').click(function(){
                  var val = $(this).data('value');
                  $('input[name=language]').val(val);
                });
                $j('.date').datepicker({ dateFormat: "dd-mm-yy", firstDay: 0, changeMonth: true, changeYear: true, yearRange: '1940:2040' });
                $('#message_1').hide();
                $('.submit_button').click(function(e){
                  e.preventDefault();
                  var error='';
                  if($('textarea[name=cancer_related_txt]').hasClass('required')){
                    $('textarea[name=cancer_related_txt]').css('background-color', '#FFCCCC');
                  }else{
                    $('textarea[name=cancer_related_txt]').css('background-color', '#dedede');
                  }
                  $('.required').each(function(){
                    if($(this).val().length==0){
                      error='Debe completar los campos obligatorios.<br />';
                      $(this).css('background-color', '#FFCCCC');
                    }else{
                      $(this).css('background-color', '#dedede');
                    }
                  });
                  if(!($('label#terms').hasClass('active'))){
                    error+='Debe aceptar los términos y condiciones.';
                  }
                  if(error.length==0){
                    var data = $('#form_voluntario').serialize();
                    $.ajax({
                        type: "POST",
                        url: 'save_volunteer',
                        data: {send:"sent", data:data},
                        success: function(response){ 
                                if(response == 'ok'){
                                  $('#message_1').html('Te has registrado con éxito.').css('background-color', 'green').show();
                                  $('input[type=text], input[type=email]').val('');
                                  $('textarea').val('');
                                }else{
                                  $('#message_1').html(response).css('background-color', 'red').show();
                                  setTimeout(function(){
                                    $('#message_1').hide();
                                  },3000);
                                }
                              },
                    });
                  }else{
                    console.log(error);
                    $('#message_1').html(error).show();
                    setTimeout(function(){
                      $('#message_1').hide();
                    },3000);
                  }
                });
              });
            </script>

            